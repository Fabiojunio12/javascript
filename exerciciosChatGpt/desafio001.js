// Entrada de dados
let numero1 = 500;
let numero2 = 300;

// Processamento de dados
let resultadoSoma = numero1 + numero2;
let resultadoSubtracao = numero1 - numero2;
let resultadoMultiplicacao = numero1 * numero2;
let resultadoDivisao = numero1 / numero2;

// Saída de dados
console.log(`A soma entre ${numero1} + ${numero2} é igual: ${resultadoSoma}.`);
console.log(`A subtração entre ${numero1} - ${numero2} é igual: ${resultadoSubtracao}.`);
console.log(`A multiplicação entre ${numero1} * ${numero2} é igual: ${resultadoMultiplicacao}.`);
console.log(`A divisão entre ${numero1} / ${numero2} é igual: ${resultadoDivisao}.`);
